export default class C_Stats {
    name = 'stats';

    constructor( STR, DEX, INT, HP, DEF, AP) {
        this.STR = STR;
        this.STR_MAX = STR;

        this.DEX = DEX;
        this.DEX_MAX = DEX;

        this.INT = INT;
        this.INT_MAX = INT;


        this.HP = HP;
        this.HP_MAX = HP;

        this.DEF = DEF;
        this.AP = AP;
    }
}